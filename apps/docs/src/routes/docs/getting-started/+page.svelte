<h1>Introduction</h1>
<p>A classless CSS framework that styles semantic HTML elements directly.</p>

<h2>What is launch.css?</h2>
<p>
	launch.css is a lightweight CSS framework designed for speed and simplicity.
	Unlike traditional CSS frameworks that require memorizing class names,
	launch.css styles your HTML elements directly based on their semantic
	meaning.
</p>

<p>
	Simply write semantic HTML, and launch.css handles the styling. No classes
	needed for basic elements.
</p>

<h2>Key Features</h2>
<ul>
	<li>
		<strong>Classless by design</strong> - Style semantic HTML elements
		directly
	</li>
	<li>
		<strong>ARIA-powered components</strong> - Uses accessibility attributes
		for styling variants
	</li>
	<li>
		<strong>Automatic dark mode</strong> - Built-in light/dark theme support
		via CSS <code>color-scheme</code>
	</li>
	<li><strong>Zero JavaScript</strong> - Pure CSS, no runtime required</li>
	<li><strong>Tiny footprint</strong> - Minimal CSS for maximum impact</li>
</ul>

<h2>Philosophy</h2>
<p>
	launch.css embraces the web platform. Instead of fighting HTML semantics
	with utility classes, it works with them. Buttons look like buttons. Links
	look like links. Forms are styled sensibly.
</p>

<p>
	When you need variants (like a danger button or secondary action),
	launch.css uses ARIA attributes you should already be using for
	accessibility. This approach encourages better HTML practices while reducing
	cognitive overhead.
</p>
